package com.sqisoft.iqs.hr.vo;

import java.util.Arrays;

public class EmpMapVO {

	private String name;
	private String team_id;
	private String team_name;
	private String position;
	private String division;
	private String pj_id;
	private String pj_start_day;
	private String startday_r;
	private String pj_end_day;
	private String endday_r;
	private String[] pj=new String[12];
	
	private String hire_date;
	private String quit_date;
	private String company;
	
	public EmpMapVO() {
		super();
	}
	public EmpMapVO(	String name, String team_id, String team_name,
						String position, String division, String pj_id,
						String pj_start_day, String startday_r, String pj_end_day,
						String endday_r, String[] pj, String hire_date, String quit_date,
						String company) {
		super();
		this.name = name;
		this.team_id = team_id;
		this.team_name = team_name;
		this.position = position;
		this.division = division;
		this.pj_id = pj_id;
		this.pj_start_day = pj_start_day;
		this.startday_r = startday_r;
		this.pj_end_day = pj_end_day;
		this.endday_r = endday_r;
		this.pj = pj;
		this.hire_date = hire_date;
		this.quit_date = quit_date;
		this.company = company;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTeam_id() {
		return team_id;
	}
	public void setTeam_id(String team_id) {
		this.team_id = team_id;
	}
	public String getTeam_name() {
		return team_name;
	}
	public void setTeam_name(String team_name) {
		this.team_name = team_name;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getDivision() {
		return division;
	}
	public void setDivision(String division) {
		this.division = division;
	}
	public String getPj_id() {
		return pj_id;
	}
	public void setPj_id(String pj_id) {
		this.pj_id = pj_id;
	}
	public String getPj_start_day() {
		return pj_start_day;
	}
	public void setPj_start_day(String pj_start_day) {
		this.pj_start_day = pj_start_day;
	}
	public String getStartday_r() {
		return startday_r;
	}
	public void setStartday_r(String startday_r) {
		this.startday_r = startday_r;
	}
	public String getPj_end_day() {
		return pj_end_day;
	}
	public void setPj_end_day(String pj_end_day) {
		this.pj_end_day = pj_end_day;
	}
	public String getEndday_r() {
		return endday_r;
	}
	public void setEndday_r(String endday_r) {
		this.endday_r = endday_r;
	}
	public String[] getpj() {
		return pj;
	}
	public String getpj(int i) {
		return pj[i];
	}
	public void setpj(String[] pj) {
		this.pj = pj;
	}
	public void setpj(int i,String pj) {
		this.pj[i] = pj;
	}

	public String getHire_date() {
		return hire_date;
	}
	public void setHire_date(String hire_date) {
		this.hire_date = hire_date;
	}
	public String getQuit_date() {
		return quit_date;
	}
	public void setQuit_date(String quit_date) {
		this.quit_date = quit_date;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}

	@Override
	public String toString() {
		return "EmpMapVO [name=" + name + ", team_id=" + team_id
				+ ", team_name=" + team_name + ", position=" + position
				+ ", division=" + division + ", pj_id=" + pj_id
				+ ", pj_start_day=" + pj_start_day + ", startday_r="
				+ startday_r + ", pj_end_day=" + pj_end_day + ", endday_r="
				+ endday_r + ", pj=" + Arrays.toString(pj) + ", hire_date="
				+ hire_date + ", quit_date=" + quit_date + ", company="
				+ company + "]";
	}
}
