package com.sqisoft.iqs.humanResource.vo;

public class HumanResourcePriceVO {
	
	//human_price 직급단가.
	private String position;
	private String monCost;
	private String monPrice;
	
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getMonCost() {
		return monCost;
	}
	public void setMonCost(String monCost) {
		this.monCost = monCost;
	}
	public String getMonPrice() {
		return monPrice;
	}
	public void setMonPrice(String monPrice) {
		this.monPrice = monPrice;
	}
	

}
